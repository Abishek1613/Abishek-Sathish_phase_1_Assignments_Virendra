package program.java.datatype;

public class DataTypeProgram {
	public static void main (String[] args){
		boolean status = true;
		System.out.println("I am printing status :"+ status);
		char gender = 'M';
		System.out.println("I am printing gender of john :"+ gender);
		long distance = 9823498732L;
		System.out.println("I am printing distance :"+ distance);
		float percentage = 70;
		System.out.println(" john's score in mathematics :"+ percentage);
		double pi = 3.14888839;
		System.out.println(" value of pi :"+ pi);
		
	}

}
